-- Analyze impact of delivery performance on customer ratings
-- Compare average review score for late vs on-time deliveries
SELECT 
CASE WHEN o.order_delivered_customer_date > o.order_estimated_delivery_date
THEN 'Late'                   -- Orders delivered after expected date
ELSE 'On Time'                -- Orders delivered on or before expected date
END AS delivery_status,
AVG(r.review_score) as avg_rating    -- Average customer rating
FROM orders o
JOIN reviews r ON o.order_id = r.order_id   -- Join orders with reviews
GROUP BY delivery_status;



-- Analyze how product weight impacts delivery (freight) cost
-- Group products into weight categories and calculate average freight 
SELECT 
CASE WHEN product_weight_g < 500 THEN 'Light'        -- Lightweight products
WHEN product_weight_g BETWEEN 500 AND 2000 THEN 'Medium'  -- Medium weight
ELSE 'Heavy'                                            -- Heavy products
END AS weight_category,
AVG(freight_value) as avg_freight         -- Average delivery cost
FROM order_items oi
JOIN products p ON oi.product_id = p.product_id    -- Join to get product weight
GROUP BY weight_category;                         -- Group by weight category

-- Analyze monthly revenue trend over time
-- Helps understand business growth and seasonality
SELECT 
DATE_TRUNC('month', order_purchase_timestamp) as month,  -- Extract month from order date
SUM(p.payment_value) as revenue                    -- Total revenue for each month
from orders o
JOIN payments p ON o.order_id = p.order_id        -- Join to get payment data
GROUP BY month                                    -- Group by month
ORDER BY month;

-- Identify top customers based on total spending
-- Helps find high-value (VIP) customers
SELECT c.customer_unique_id,    -- Unique customer identifier
SUM(p.payment_value) as total_spent    -- Total amount spent by customer
FROM orders o
JOIN payments p ON o.order_id = p.order_id    -- Join payments with orders
JOIN customers c ON o.customer_id = c.customer_id   -- Map orders to customers
GROUP BY c.customer_unique_id                      -- Group by unique customer
ORDER BY total_spent DESC 
LIMIT 10;

-- Calculate Average Order Value (AOV)
-- Measures average spending per order
SELECT 
sum(payment_value) / COUNT(DISTINCT order_id) as avg_order_value
FROM payments;




-- Calculate average delivery time for completed orders
