-- Categorize orders into Delivered and Not Delivered
-- Helps understand overall order completion rate
SELECT 
CASE WHEN order_status = 'delivered'   
THEN 'Delivered'                     -- Successfully delivered orders
ELSE 'Not Delivered' END AS status,
COUNT(*) 							 -- Count of orders in each category
FROM orders
GROUP BY status;



-- Filter reviews where review score is available
SELECT * FROM reviews
WHERE review_score IS NOT NULL;


-- Categorize customer reviews into sentiment groups
-- Helps understand overall customer satisfaction
SELECT 
CASE WHEN review_score >= 4 THEN 'Positive'     -- High ratings (satisfied customers)
WHEN review_score = 3 THEN 'Neutral' 		    -- average experience
ELSE 'Negative'								    -- Low ratings (unsatisfied customers)
END as sentiment,
COUNT(*) AS count
FROM reviews
GROUP BY sentiment
ORDER BY count DESC;

