-- Calculate total revenue generated by each state
-- Join payments, orders, and customers to map revenue to customer location

SELECT 
c.customer_state,					                 -- Customer's state									
SUM(p.payment_value) as revenue			             -- Total revenue from that state
FROM payments p
JOIN orders o ON p.order_id = o.order_id             -- Link payments to orders
JOIN customers c ON o.customer_id = c.customer_id	 -- Link orders to customers (for location)
GROUP BY c.customer_state							 -- Group results by state
ORDER BY revenue DESC limit 10;						 -- Sort from highest to lowest revenue l

-- Calculate revenue generated by each product category
-- Join order_items, products, and category translation table
SELECT 
ct.product_category_name_english,      -- Product category (in English)
SUM(oi.price) as revenue               -- Total revenue from that category
from order_items oi
JOIN products p ON oi.product_id = p.product_id   -- Link order items to products
JOIN category_names ct
ON p.product_category_name = ct.product_category_name   -- Translate category name
GROUP BY ct.product_category_name_english
ORDER BY revenue DESC LIMIT 10;

